# Scrivere una funzione che, dato in ingresso un numero intero positivo,
# restituisca il numero intero più vicino alla sua radice quadrata
# senza usare le funzioni della libreria math.

# Inizializzazione:
numero_intero = int(input("Inserisci un numero intero positivo ---> "))
radice_quadrata = numero_intero ** .5
print(f"Il numero intero più vicino alla radice quadrata del numero dato in ingresso è: {round(radice_quadrata)}")

