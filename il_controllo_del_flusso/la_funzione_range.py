# 1) Generatore delle cifre del sistema di numerazione posizionale in base dieci.
for i in range(10):
    print(i, end=' ')
print()

# 2) Generatore delle cifre del sistema di numerazione posizionale in base dieci comprese tra quattro e nove.
for i in range(4, 9):
    print(i, end=' ')
print()

# 3) Generatore delle cifre dispari del sistema di numerazione posizionale in base dieci.
for i in range(1, 10, 2):
    print(i, end=' ')
print()

# 4) Generatore delle cifre negative del sistema di numerazione posizionale in base dieci.
for i in range(0, -10, -1):
    print(i, end=' ')
